#include "common.h"

void	ra(t_ps *ps)
{
	if (ps->a)
		ps->a = ps->a->next;
}

void	rb(t_ps *ps)
{
	if (ps->b)
		ps->b = ps->b->next;
}

void	rr(t_ps *ps)
{
	ra(ps);
	rb(ps);
}
