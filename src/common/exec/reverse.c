#include "common.h"

void	rra(t_ps *ps)
{
	if (ps->a)
		ps->a = ps->a->prev;
}

void	rrb(t_ps *ps)
{
	if (ps->b)
		ps->b = ps->b->prev;
}

void	rrr(t_ps *ps)
{
	rra(ps);
	rrb(ps);
}
